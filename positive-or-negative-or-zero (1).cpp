// 6. Write a Program to find whether the given number is 
//  positive or negative or zero. 

#include <iostream>

using namespace std;

int main()
{
    int n;
    cout<<"enter the value of n is : ";
    cin>>n;
    if(n>0)
    cout<<"positive";
    else if(n<0)
    cout<<"negative";
    else
    cout<<"zero";
}
